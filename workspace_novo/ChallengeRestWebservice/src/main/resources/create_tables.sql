
CREATE TABLE municipio(
id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
municipio_desc VARCHAR(200),
estado VARCHAR(2));

CREATE TABLE cep(
cep_cod VARCHAR(8),
id_mun INTEGER,
bairro VARCHAR(200),
rua VARCHAR(300));

CREATE TABLE endereco(
id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
cep_cod VARCHAR(8),
numero INTEGER,
bairro VARCHAR(200),
rua VARCHAR(300),
complemento VARCHAR(300));

